#include "../../inc/libft.h"
/**The ft_isalpha function checks if the given character is an alphabet letter.
 It returns the character if it's a letter (A-Z, a-z), otherwise it returns 0.*/
int	ft_isalpha(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (c);
	return (0);
}
